// Simple hello world program
#include <stdio.h>

int main(void)
{
    // Prints hello, world once
    printf("hello, world\n");

    // Loop that forever prints hello world
    while (true)
    {
        printf("hello, world\n");
    }

    // Loop that prints hello, world 50 times
    for (int i = 0; i < 50; i++)
    {
        printf("hello, world\n");
    }

}
